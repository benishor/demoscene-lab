#include <MeshBuilder.h>

Mesh MeshBuilder::cube() {
    Mesh result;
    result.vertices = {
        { -0.5, -0.5, -0.5},
        { 0.5, -0.5, -0.5},
        { 0.5,  0.5, -0.5},
        { -0.5,  0.5, -0.5},

        { -0.5, -0.5,  0.5},
        { 0.5, -0.5,  0.5},
        { 0.5,  0.5,  0.5},
        { -0.5,  0.5,  0.5},
    };

    result.edges = {
        {0, 1},
        {1, 2},
        {2, 3},
        {3, 0},
        {4, 5},
        {5, 6},
        {6, 7},
        {7, 4},
        {0, 4},
        {1, 5},
        {2, 6},
        {3, 7}
    };

    result.facets = {
        {0, 1, 2},
        {2, 3, 0},
        {5, 4, 7},
        {7, 6, 5},
        {1, 5, 6},
        {6, 2, 1},
        {4, 0, 3},
        {3, 7, 4},
        {4, 5, 1},
        {1, 0, 4},
        {3, 2, 6},
        {6, 7, 3}
    };
    return result;
}
